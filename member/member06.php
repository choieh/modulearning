						<li>③ 수강자가 제1항 제4호(수강료등의 허위게시 또는 초과징수)의 사유에 의하여 계약을 해제한 경우에는 수강증을 학원에 반환하고, 학원은 지체없이 다음 각호의 금액을 수강자에게 환급합니다.
						<ol class="depth2">
						<li>1. 수강자가 당해 사유를 안 후 지체없이 해제한 경우<br></li>
						<li class="formula"><span class="rnum">&nbsp;&nbsp;&nbsp;</span>
